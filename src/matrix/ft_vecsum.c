#include "matrix.h"

void	ft_vec4sum(t_vec4 a, t_vec4 b)
{
	a[0] += b[0];
	a[1] += b[1];
	a[2] += b[2];
	a[3] += b[3];
}

void	ft_vec3sum(t_vec3 a, t_vec3 b)
{
	a[0] += b[0];
	a[1] += b[1];
	a[2] += b[2];
}

void	ft_vec2sum(t_vec2 a, t_vec2 b)
{
	a[0] += b[0];
	a[1] += b[1];
}
