#include "matrix.h"

float	ft_vec4norm(t_vec4 v)
{
	return (sqrt((v[0] * v[0]) + (v[1] * v[1]) + (v[2] * v[2]) +
	(v[3] * v[3])));
}

float	ft_vec3norm(t_vec3 v)
{
	return (sqrt((v[0] * v[0]) + (v[1] * v[1]) + (v[2] * v[2])));
}

float	ft_vec2norm(t_vec2 v)
{
	return (sqrt((v[0] * v[0]) + (v[1] * v[1])));
}
