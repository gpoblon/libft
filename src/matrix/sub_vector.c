#include "matrix.h"

void	sub_vector4f(fvect4 a, fvect4 b)
{
	a[0] -= b[0];
	a[1] -= b[1];
	a[2] -= b[2];
	a[3] -= b[3];
}

void	sub_vector3f(fvect3 a, fvect3 b)
{
	a[0] -= b[0];
	a[1] -= b[1];
	a[2] -= b[2];
}

void	sub_vector2f(fvect2 a, fvect2 b)
{
	a[0] -= b[0];
	a[1] -= b[1];
}
