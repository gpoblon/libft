#include "str.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n && s1[i] && s2[i] && s1[i] == s2[i])
		++i;
	if (i == n)
		return (0);
	return ((t_uchar)s1[i] - (t_uchar)s2[i]);
}
